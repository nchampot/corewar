#include "libft.h"

int		ft_max(int *tab, int len)
{
	int i;
	int n;

	n = 0;
	i = tab[0];
	while (n < len)
	{
		if (i < tab[n])
			i = tab[n];
		n++;
	}
	return (i);
}
